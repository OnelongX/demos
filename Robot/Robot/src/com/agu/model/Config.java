public class Config {

    private  String server ;

    private  String port ;

    private  String db ;

    private  String table ;

    private  String userName ;

    private  String password;

    private  String basePackage;

    private  String plate;

    private  String modelCn;

    private  String modelName;

    private  String functionCn;

    private  String functionName;

    public Config() {

    }

    public String getServer() {
        return server;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getDb() {
        return db;
    }

    public void setDb(String db) {
        this.db = db;
    }

    public String getTable() {
        return table;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getBasePackage() {
        return basePackage;
    }

    public void setBasePackage(String basePackage) {
        this.basePackage = basePackage;
    }

    public String getPlate() {
        return plate;
    }

    public void setPlate(String plate) {
        this.plate = plate;
    }

    public String getModelCn() {
        return modelCn;
    }

    public void setModelCn(String modelCn) {
        this.modelCn = modelCn;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public String getFunctionCn() {
        return functionCn;
    }

    public void setFunctionCn(String functionCn) {
        this.functionCn = functionCn;
    }

    public String getFunctionName() {
        return functionName;
    }

    public void setFunctionName(String functionName) {
        this.functionName = functionName;
    }



}
